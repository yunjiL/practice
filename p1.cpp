#include <iostream>

using namespace std;

int main()
{
    int n1, n2, n3;
    cin >> n1;
    cin >> n2;
    cin >> n3;
    if (n1 % 2 == 0)
        cout << "even" << endl;
    else cout << "odd" << endl;
    if (n2 % 2 == 0)
        cout << "even" << endl;
    else cout << "odd" << endl;
    if (n3 % 2 == 0)
        cout << "even" << endl;
    else cout << "odd" << endl;
}